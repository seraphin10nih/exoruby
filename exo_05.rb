#{} cette instruction sert inserer des chaines de caractères ou integer dans une variable

# cette instruction permet d'afficher la variable travail avec le produit des chiffres dans les {}
puts "Travail : #{10 * 5 * 11}"

# comme l'instruction ci-dessus ceci affichera le produit de ces chiffres après la variable
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

# cette instruction permet d'afficher Et en secondes ?
puts "Et en secondes ?"

# ceci permet d'afficher le produit de ces chiffres ci-dessous
puts 10 * 5 * 11 * 60 * 60

# cette instruction permet d'afficher cette chaîne de caractères "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"

# cette instruction demande un bouléen true ou false des integers en bas
puts 3 + 2 < 5 - 7

# cette instruction affiche les chaines suivies de la somme entre #{}
puts "Ça fait combien 3 + 2 ? #{3 + 2}"
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

# cette instruction permet d'afficher Ok, c'est faux alors !
puts "Ok, c'est faux alors !"

# # cette instruction permet d'afficher C'est drôle ça, faisons-en plus :
puts "C'est drôle ça, faisons-en plus :"

# cette instruction permet d'afficher les chaines de caractères ici en jaune suivies de bouléen répondant la comparaison dans #{}
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}"
